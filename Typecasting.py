# type casting - convert the data type of value to another data type.
# this is important because printed values can't have for example integer and string next to each other
x = 1   # integer
y = 2.0   # float
z = "3"   # string

x = float(x)
y = int(y)
z = int(z)

print(x)
print(y)
print(z*3)

